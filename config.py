import json, os

CONFIG_DIR = os.path.join(os.path.dirname(__file__), 'config')

def load_config(name: str) -> dict:
    model_config_path = os.path.join(CONFIG_DIR, f"{name}.json")
    with open(model_config_path, 'r') as f:
        return json.load(f)
    
